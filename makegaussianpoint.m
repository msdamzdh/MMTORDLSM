function [r,W]=makegaussianpoint(ng)

switch ng
        case 1
                r=0;W=2;
        case 2
                r=[-0.577,0.577];W=[1,1];
        case 3
                r=[-0.774 0 0.774];W=[0.555 0.888 0.555];
        case 4
                r=[-0.861 -0.34 0.34 0.861];W=[0.348 0.652 0.652 0.348];
        case 5
                r=[-0.906 -0.538 0 0.538 0.906];W=[0.237 0.478 0.568 0.478 0.237];
end
end